function Fingerprint(hasher){
	var nativeForEach = Array.prototype.forEach;
	var nativeMap = Array.prototype.map;
	this.each = function(obj, iterator, context) {
	  if (obj == null) return;
	  if (nativeForEach && obj.forEach === nativeForEach) {
		obj.forEach(iterator, context);
	  } else if (obj.length === +obj.length) {
		for (var i = 0, l = obj.length; i < l; i++) {
		  if (JSON.stringify(iterator.call(context, obj[i], i, obj)) === '{}' ) return;
		}
	  } else {
		for (var key in obj) {
		  if (obj.hasOwnProperty(key)) {
			if ( JSON.stringify(iterator.call(context, obj[key], key, obj)) === '{}' ) return;
		  }
		}
	  }
	};
	this.map = function(obj, iterator, context) {
	  var results = [];
	  if (obj == null) return results;
	  if (nativeMap && obj.map === nativeMap) return obj.map(iterator, context);
	  this.each(obj, function(value, index, list) {
		results[results.length] = iterator.call(context, value, index, list);
	  });
	  return results;
	};
  
	if(hasher){
	  this.hasher = hasher;
	}
  }
  
  Fingerprint.prototype = {
  
	get: function(){
	  keys = [];
	  keys.push(navigator.userAgent);
	  keys.push([screen.height, screen.width, screen.colorDepth].join('x'));
	  keys.push(new Date().getTimezoneOffset());
	  keys.push(!!window.sessionStorage);
	  keys.push(!!window.localStorage);
	  var pluginsString = this.map(navigator.plugins, function(p){
		var mimeTypes = this.map(p, function(mt){
		  return [mt.type, mt.suffixes].join('~');
		}).join(',');
		return [p.name, p.description, mimeTypes].join('::');
	  }, this).join(';');
	  keys.push(pluginsString);
	  if(this.hasher){
		return this.hasher(keys.join('###'), 31);
	  } else {
		return this.murmurhash3_32_gc(keys.join('###'), 31);
	  }
	},
  
	/**
	 * JS Implementation of MurmurHash3 (r136) (as of May 20, 2011)
	 * 
	 * @author <a href="mailto:gary.court@gmail.com">Gary Court</a>
	 * @see http://github.com/garycourt/murmurhash-js
	 * @author <a href="mailto:aappleby@gmail.com">Austin Appleby</a>
	 * @see http://sites.google.com/site/murmurhash/
	 * 
	 * @param {string} key ASCII only
	 * @param {number} seed Positive integer only
	 * @return {number} 32-bit positive integer hash 
	 */
  
	murmurhash3_32_gc: function(key, seed) {
	  var remainder, bytes, h1, h1b, c1, c1b, c2, c2b, k1, i;
	  
	  remainder = key.length & 3; // key.length % 4
	  bytes = key.length - remainder;
	  h1 = seed;
	  c1 = 0xcc9e2d51;
	  c2 = 0x1b873593;
	  i = 0;
	  
	  while (i < bytes) {
		  k1 = 
			((key.charCodeAt(i) & 0xff)) |
			((key.charCodeAt(++i) & 0xff) << 8) |
			((key.charCodeAt(++i) & 0xff) << 16) |
			((key.charCodeAt(++i) & 0xff) << 24);
		++i;
		
		k1 = ((((k1 & 0xffff) * c1) + ((((k1 >>> 16) * c1) & 0xffff) << 16))) & 0xffffffff;
		k1 = (k1 << 15) | (k1 >>> 17);
		k1 = ((((k1 & 0xffff) * c2) + ((((k1 >>> 16) * c2) & 0xffff) << 16))) & 0xffffffff;
  
		h1 ^= k1;
			h1 = (h1 << 13) | (h1 >>> 19);
		h1b = ((((h1 & 0xffff) * 5) + ((((h1 >>> 16) * 5) & 0xffff) << 16))) & 0xffffffff;
		h1 = (((h1b & 0xffff) + 0x6b64) + ((((h1b >>> 16) + 0xe654) & 0xffff) << 16));
	  }
	  
	  k1 = 0;
	  
	  switch (remainder) {
		case 3: k1 ^= (key.charCodeAt(i + 2) & 0xff) << 16;
		case 2: k1 ^= (key.charCodeAt(i + 1) & 0xff) << 8;
		case 1: k1 ^= (key.charCodeAt(i) & 0xff);
		
		k1 = (((k1 & 0xffff) * c1) + ((((k1 >>> 16) * c1) & 0xffff) << 16)) & 0xffffffff;
		k1 = (k1 << 15) | (k1 >>> 17);
		k1 = (((k1 & 0xffff) * c2) + ((((k1 >>> 16) * c2) & 0xffff) << 16)) & 0xffffffff;
		h1 ^= k1;
	  }
	  
	  h1 ^= key.length;
  
	  h1 ^= h1 >>> 16;
	  h1 = (((h1 & 0xffff) * 0x85ebca6b) + ((((h1 >>> 16) * 0x85ebca6b) & 0xffff) << 16)) & 0xffffffff;
	  h1 ^= h1 >>> 13;
	  h1 = ((((h1 & 0xffff) * 0xc2b2ae35) + ((((h1 >>> 16) * 0xc2b2ae35) & 0xffff) << 16))) & 0xffffffff;
	  h1 ^= h1 >>> 16;
  
	  return h1 >>> 0;
	}
}